/***********************************************************************************************************************************
 *** IMPLEMENTACION DE FUNCIONES PUBLICAS
 **********************************************************************************************************************************/

gpio_pin_config_t config =
{
	kGPIO_DigitalOutput,
 	1,
};

void Rele_Init()
{
	GPIO_PortInit(GPIO, PORT_1);
	GPIO_PortInit(GPIO, PORT_0);
	GPIO_PinInit(GPIO, PORT_1, LIGHT_0, &config);
	GPIO_PinInit(GPIO, PORT_0, RELE_0, &config);
}

void EncenderAlarma()
{
	GPIO_PinWrite(GPIO, PORT_1, LIGHT_0, RELE_ON);
}

void ApagarAlarma()
{
	GPIO_PinWrite(GPIO, PORT_1, LIGHT_0, RELE_OFF);
}

void SubirPersiana()
{
	GPIO_PinWrite(GPIO, PORT_0, RELE_0, RELE_ON);
}

void BajarPersiana()
{
	GPIO_PinWrite(GPIO, PORT_0, RELE_0, RELE_OFF);
}

